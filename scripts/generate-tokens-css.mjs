/**
 * Generate tokens.css and tokens-dark.css from tokens.json
 *
 * These are the light-only and dark-override token layers (no badge or
 * taxonomy-badge colors — those live in root.css / taxonomy-badges.css).
 * They were previously hand-maintained and drifted out of sync with
 * tokens.json; this generator makes them a derived artifact like root.css
 * so they can never drift again.
 *
 * One source (tokens.json), generated outputs:
 *   css/tokens.css       — light :root {}
 *   css/tokens-dark.css  — @media (prefers-color-scheme: dark) overrides
 * (css/tokens-all.css is a static two-line @import wrapper and is left as-is.)
 *
 * Usage:
 *   node scripts/generate-tokens-css.mjs                              # prints light to stdout
 *   node scripts/generate-tokens-css.mjs --out css/tokens.css \
 *        --out-dark css/tokens-dark.css                               # writes both files
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';

const ROOT = dirname( new URL( import.meta.url ).pathname );
const PROJECT = join( ROOT, '..' );
const tokensJson = JSON.parse( readFileSync( join( PROJECT, 'tokens.json' ), 'utf-8' ) );

// ─── Category config ─────────────────────────────────────────────────────────
// Mirrors generate-root-css.mjs ordering, but intentionally EXCLUDES the
// `badge` and `taxonomy-badge` categories — those belong to root.css /
// taxonomy-badges.css, not the base token layer.

const categoryConfig = [
	{ key: 'color', label: 'Colors', type: 'color' },
	{ key: 'typography', label: 'Typography', type: 'static' },
	{ key: 'font-size', label: 'Font Sizes', type: 'static' },
	{ key: 'font-weight', label: 'Font Weights', type: 'static' },
	{ key: 'border-radius', label: 'Border Radius', type: 'static' },
	{ key: 'layout', label: 'Layout', type: 'static' },
	{ key: 'line-height', label: 'Line Heights', type: 'static' },
	{ key: 'spacing', label: 'Spacing', type: 'static' },
	{ key: 'motion', label: 'Motion / Transitions', type: 'static' },
	{ key: 'badge', label: 'Badge Colors', type: 'static' },
];

// ─── Build light + dark lines ────────────────────────────────────────────────

const lightLines = [];
const darkLines = [];

for ( const { key, label, type } of categoryConfig ) {
	const tokens = tokensJson.categories[ key ];
	if ( ! tokens ) continue;

	lightLines.push( `    /* ===== ${ label } ===== */` );

	for ( const [ name, token ] of Object.entries( tokens ) ) {
		const varName = `--${ name }`;
		if ( type === 'color' ) {
			const lightValue = token.cssRef || token.light;
			lightLines.push( `    ${ varName }: ${ lightValue };` );

			if ( token.cssRef ) {
				darkLines.push( `        ${ varName }: ${ token.cssRef };` );
			} else if ( token.dark !== token.light ) {
				darkLines.push( `        ${ varName }: ${ token.dark };` );
			}
		} else {
			lightLines.push( `    ${ varName }: ${ token.value };` );
		}
	}

	lightLines.push( '' );
}

if ( lightLines[ lightLines.length - 1 ] === '' ) {
	lightLines.pop();
}

// ─── Assemble outputs ────────────────────────────────────────────────────────

const lightHeader = `/**
 * Extra Chill Design Tokens — Light Theme
 *
 * Source of truth: tokens.json
 * Package: @extrachill/tokens
 *
 * Generated by scripts/generate-tokens-css.mjs — do not edit by hand.
 *
 * Import this file for light-mode-only tokens.
 * For light + dark (with prefers-color-scheme), use tokens-all.css instead.
 */
`;

const darkHeader = `/**
 * Extra Chill Design Tokens — Dark Theme Overrides
 *
 * Source of truth: tokens.json
 * Package: @extrachill/tokens
 *
 * Generated by scripts/generate-tokens-css.mjs — do not edit by hand.
 *
 * Import this file alongside tokens.css for manual dark mode control.
 * For automatic prefers-color-scheme switching, use tokens-all.css instead.
 */
`;

const lightOutput = [
	lightHeader,
	':root {',
	...lightLines,
	'}',
	'',
].join( '\n' );

const darkOutput = [
	darkHeader,
	'@media (prefers-color-scheme: dark) {',
	'    :root {',
	...darkLines,
	'    }',
	'}',
	'',
].join( '\n' );

// ─── Write or print ─────────────────────────────────────────────────────────

const outFlag = process.argv.indexOf( '--out' );
const outDarkFlag = process.argv.indexOf( '--out-dark' );

if ( outFlag !== -1 && process.argv[ outFlag + 1 ] ) {
	const outPath = join( PROJECT, process.argv[ outFlag + 1 ] );
	writeFileSync( outPath, lightOutput, 'utf-8' );
	console.log( `✅ Generated ${ outPath }` );

	if ( outDarkFlag !== -1 && process.argv[ outDarkFlag + 1 ] ) {
		const outDarkPath = join( PROJECT, process.argv[ outDarkFlag + 1 ] );
		writeFileSync( outDarkPath, darkOutput, 'utf-8' );
		console.log( `✅ Generated ${ outDarkPath }` );
	}
} else {
	process.stdout.write( lightOutput );
}
